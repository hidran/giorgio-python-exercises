# Programma per calcolare l'interesse del 30% su una somma inserita dall'utente

# Richiede l'input all'utente
somma = float(input("Inserisci la somma di denaro: "))
interesse = float(input("inserisci interesse"))
# Calcola l'interesse (30% della somma)
risultato = somma * interesse

# Stampa il risultato
print("Interesse calcolato =", risultato)

